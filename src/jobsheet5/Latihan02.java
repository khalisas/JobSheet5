
package jobsheet5;
public class Latihan02 {
    public static void main (String[]args){
    int a = 10;
    int b = 2;
    
    int c;
     
    // Penjumlahan 
    c = a + b;
    System.out.println(a + " + " + b + " = " +c);
    
    // Pengurangan
    c = a - b;
    System.out.println(a + " - " + b + " = " +c);
    
    // Perkalian 
    c = a * b;
    System.out.println(a + " x " + b + " = " +c);
    
    // Pembagian
    c = a / b;
    System.out.println(a + " / " + b + " = " +c);
    
    // Sisa pembagian
    c = a % b;
    System.out.println(a + " % " + b + " = " +c);
        
        }
    }
    

